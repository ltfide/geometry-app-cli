package kawah.edukasi.entity.bangundatar;

public class JajarGenjang {

    private int luas;
    private int keliling;
    private int alas;
    private int tinggi;
    private int sisisejajar;
    private int sisialas;
    private int sisimiring;

    public JajarGenjang() {
    }

    public int getLuas() {
        return luas;
    }

    public void setLuas(int luas) {
        this.luas = luas;
    }

    public int getKeliling() {
        return keliling;
    }

    public void setKeliling(int keliling) {
        this.keliling = keliling;
    }

    public int getAlas() {
        return alas;
    }

    public void setAlas(int alas) {
        this.alas = alas;
    }

    public int getTinggi() {
        return tinggi;
    }

    public void setTinggi(int tinggi) {
        this.tinggi = tinggi;
    }

    public int getSisisejajar() {
        return sisisejajar;
    }

    public void setSisisejajar(int sisisejajar) {
        this.sisisejajar = sisisejajar;
    }

    public int getSisialas() {
        return sisialas;
    }

    public void setSisialas(int sisialas) {
        this.sisialas = sisialas;
    }

    public int getSisimiring() {
        return sisimiring;
    }

    public void setSisimiring(int sisimiring) {
        this.sisimiring = sisimiring;
    }
}
