package kawah.edukasi.entity.bangunruang;

public class Kubus {

    private int sisi;
    private int rusukkubus;
    private int luas;

    public Kubus() {
    }

    public int getSisi() {
        return sisi;
    }

    public void setSisi(int sisi) {
        this.sisi = sisi;
    }

    public int getRusukkubus() {
        return rusukkubus;
    }

    public void setRusukkubus(int rusukkubus) {
        this.rusukkubus = rusukkubus;
    }

    public int getLuas() {
        return luas;
    }

    public void setLuas(int luas) {
        this.luas = luas;
    }
}
